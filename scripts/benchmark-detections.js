#!/usr/bin/env node

/**
 * Benchmark Detection Script
 *
 * Runs each detection module's four-step pipeline (detect → buildKrtItems →
 * enrich → dedupe) against the demo manuscripts and produces an XLSX file
 * comparing real results vs the pre-computed demo data.
 *
 * Detection types covered: das, markdown, protocols, datasets, software,
 * materials, identifiers.
 *
 * Usage:
 *   node scripts/benchmark-detections.js [options]
 *
 * Options:
 *   --concurrency N       Max parallel manuscripts (default: 3)
 *   --manuscript ID       Run only this manuscript (case-insensitive)
 *   --type t1,t2          Only run these types
 *                         (das,markdown,protocols,datasets,software,materials,identifiers)
 *   --skip t1,t2          Skip these types
 *   --no-krt-only         Only run on manuscripts that have no matching KRT file
 *   --output path         Output file path (default: benchmark-results-YYYY-MM-DD.xlsx)
 *   --help                Show this help
 *
 * Output sheets:
 *   - Summary       per-manuscript counts (demo vs real) per detector
 *   - real          flat list of NER/detector resources (legacy shape)
 *   - demo          flat list of demo resources (legacy shape)
 *   - krt-entries   canonical KrtEntry rows (one per item, all detectors) —
 *                   this is what each module emits AFTER its dedup step
 *
 * Environment:
 *   Same env vars as the detection services (API keys, etc.). Missing configs
 *   cause that detection type to be skipped (not a fatal error).
 *
 * Enrichment for the pipeline detectors comes from the curated CSVs in
 *   tmp/identifiers/curated-{software,materials,datasets,protocols}.csv
 *   (generated by tmp/identifiers/build-curated-csvs.js)
 * so the benchmark runs without a database. Missing CSVs degrade enrichment
 * to a no-op (matching=false on every item) but the rest still works.
 */

const fs = require('fs');
const path = require('path');

require('dotenv').config({ path: path.join(__dirname, '../.env') });

const XLSX = require('xlsx');

// Paths
const DEMO_FINDINGS_DIR = path.join(__dirname, '../src/backend/data/demo-findings');
const DEMO_PDF_DIR      = path.join(__dirname, '../src/frontend/public/demo-files');
const IDENTIFIERS_DIR   = path.join(__dirname, '../tmp/identifiers');

// Pipeline imports — single source of truth for what each detector emits.
const { dedupeKrtItems } = require('../src/backend/services/pdf-analysis/dedupe-krt-items.service');
const { createCsvProvider } = require('../src/backend/services/enrichment-list-providers');
const protocolsService = require('../src/backend/services/protocols/protocols.service');
const datasetsService  = require('../src/backend/services/datasets/datasets.service');
const softwareService  = require('../src/backend/services/software/software.service');
const materialsService = require('../src/backend/services/materials/materials.service');
const identifierService = require('../src/backend/services/identifier-detection/identifier-detection.service');
const knownIdentifierIndex = require('../src/backend/services/identifier-detection/known-identifier-index.service');

// ===================== CLI ARGS =====================

const args = process.argv.slice(2);

if (args.includes('--help')) {
  console.log(fs.readFileSync(__filename, 'utf-8').match(/\/\*\*([\s\S]*?)\*\//)?.[1] || '');
  process.exit(0);
}

function getArg(name, defaultValue) {
  const idx = args.indexOf(name);
  if (idx === -1 || idx + 1 >= args.length) return defaultValue;
  return args[idx + 1];
}

const CONCURRENCY = parseInt(getArg('--concurrency', '3'), 10);
const MANUSCRIPT_FILTER = getArg('--manuscript', null);
const TYPE_FILTER = getArg('--type', null)?.split(',') || null;
const SKIP_TYPES = getArg('--skip', null)?.split(',') || [];
const OUTPUT_PATH = getArg('--output', null);
const NO_KRT_ONLY = args.includes('--no-krt-only');

const ALL_TYPES = ['das', 'markdown', 'protocols', 'datasets', 'software', 'materials', 'identifiers'];

function isTypeEnabled(type) {
  if (SKIP_TYPES.includes(type)) return false;
  if (TYPE_FILTER && !TYPE_FILTER.includes(type)) return false;
  return true;
}

// CSV provider — shared across the four detectors that have a curated list.
const csvProvider = createCsvProvider(IDENTIFIERS_DIR);

// ===================== DISCOVERY =====================

function findKrtFile(manuscriptId) {
  if (!fs.existsSync(DEMO_PDF_DIR)) return null;
  const allFiles = fs.readdirSync(DEMO_PDF_DIR);
  return allFiles.find(f => {
    if (!/\.(xlsx|csv|xls|ods)$/i.test(f)) return false;
    const base = f.replace(/\.(xlsx|csv|xls|ods)$/i, '');
    return base === manuscriptId;
  }) || null;
}

function discoverManuscripts() {
  const jsonFiles = fs.readdirSync(DEMO_FINDINGS_DIR).filter(f => f.endsWith('-demo.json'));
  const manuscripts = [];
  for (const jsonFile of jsonFiles) {
    const jsonPath = path.join(DEMO_FINDINGS_DIR, jsonFile);
    const data = JSON.parse(fs.readFileSync(jsonPath, 'utf-8'));
    const msId = data.manuscriptId;
    if (!msId) continue;

    const slug = jsonFile.replace('-demo.json', '');
    const mdPath = path.join(DEMO_FINDINGS_DIR, `${slug}-demo.md`);
    const pdfPath = path.join(DEMO_PDF_DIR, `${msId}.pdf`);

    if (MANUSCRIPT_FILTER && msId.toUpperCase() !== MANUSCRIPT_FILTER.toUpperCase()) continue;

    const krtFile = findKrtFile(msId);
    if (NO_KRT_ONLY && krtFile) continue;

    manuscripts.push({
      manuscriptId: msId,
      jsonPath,
      mdPath: fs.existsSync(mdPath) ? mdPath : null,
      pdfPath: fs.existsSync(pdfPath) ? pdfPath : null,
      krtFile
    });
  }
  return manuscripts.sort((a, b) => a.manuscriptId.localeCompare(b.manuscriptId));
}

// ===================== SERVICE AVAILABILITY =====================

function checkServiceAvailability() {
  const status = {};

  const tryConfig = (key, configPath, promptPath = null) => {
    try {
      const cfg = require(configPath);
      const promptOK = !promptPath || fs.existsSync(promptPath);
      const ok = cfg.isConfigured() && promptOK;
      status[key] = {
        available: ok,
        reason: ok ? 'OK' : (!cfg.isConfigured() ? 'Not configured' : 'Prompt file missing')
      };
    } catch {
      status[key] = { available: false, reason: 'Config not found' };
    }
  };

  const PROMPTS = path.join(__dirname, '../src/backend/data/prompts');
  tryConfig('das',       '../src/backend/config/pdf-das-extractor-api');
  tryConfig('markdown',  '../src/backend/config/pdf-markdown-api');
  tryConfig('protocols', '../src/backend/config/protocols-detection-api', path.join(PROMPTS, 'protocols-detection.txt'));
  tryConfig('datasets',  '../src/backend/config/datasets-detection-api',  path.join(PROMPTS, 'datasets-consolidation.txt'));
  tryConfig('materials', '../src/backend/config/materials-detection-api', path.join(PROMPTS, 'materials-detection.txt'));
  tryConfig('software',  '../src/backend/config/softcite-api');

  // identifier-scan: local module, available iff curated CSVs exist for all
  // four categories. Missing CSVs degrade gracefully (empty index) but the
  // benchmark intentionally flags this so users know to run build-curated-csvs.
  const REQUIRED_CSVS = ['software', 'materials', 'datasets', 'protocols']
    .map(c => path.join(IDENTIFIERS_DIR, `curated-${c}.csv`));
  const missingCsvs = REQUIRED_CSVS.filter(p => !fs.existsSync(p));
  status.identifiers = missingCsvs.length === 0
    ? { available: true, reason: 'OK (curated CSVs present)' }
    : { available: false, reason: `Missing ${missingCsvs.length} curated CSV(s) in tmp/identifiers — run tmp/identifiers/build-curated-csvs.js` };

  return status;
}

// ===================== DETECTOR RUNNERS =====================
//
// Each runner invokes the full four-step pipeline (detect → buildKrtItems →
// enrich → dedupe) and returns canonical KrtEntry[]. Enrichment uses the
// CSV provider so we don't need a database.

async function runDAS(pdfBuffer, fileName) {
  const { extractDAS } = require('../src/backend/services/pdf/pdf-das-extractor-client.service');
  return (await extractDAS(pdfBuffer, fileName)) || '';
}

async function runMarkdownConvert(pdfBuffer, fileName) {
  const { convertToMarkdown } = require('../src/backend/services/pdf/pdf-markdown-client.service');
  return (await convertToMarkdown(pdfBuffer, fileName)) || '';
}

async function runProtocols(markdownText) {
  const { resources } = await protocolsService.detectProtocols(markdownText);
  const krt = protocolsService.buildKrtItemsProtocols(resources);
  return dedupeKrtItems(krt, 'protocols-gemini');
}

async function runDatasets(markdownText) {
  const { resources } = await datasetsService.detectDatasets(markdownText);
  const krt = datasetsService.buildKrtItemsDatasets(resources);
  return dedupeKrtItems(krt, 'datasets-gemini');
}

async function runSoftware(pdfBuffer, fileName) {
  const { resources } = await softwareService.detectSoftware(pdfBuffer, fileName);
  const krt = softwareService.buildKrtItemsSoftware(resources);
  return dedupeKrtItems(krt, 'software-softcite');
}

async function runMaterials(pdfBuffer, fileName) {
  const { resources } = await materialsService.detectMaterials(pdfBuffer, fileName);
  const krt = materialsService.buildKrtItemsMaterials(resources);
  return dedupeKrtItems(krt, 'materials-gemini');
}

// Identifier-scan: build the index ONCE (cached in the loadIndex service).
let _identifierIndexBuilt = false;
async function ensureIdentifierIndex() {
  if (_identifierIndexBuilt) return;
  await knownIdentifierIndex.loadIndex({ provider: csvProvider });
  _identifierIndexBuilt = true;
}

async function runIdentifiers(markdownText) {
  await ensureIdentifierIndex();
  const index = await knownIdentifierIndex.loadIndex({ provider: csvProvider });
  const { matches, scannedLength, referencesCutoff } = identifierService.detectIdentifiers(markdownText, index);
  const krt = identifierService.buildKrtItemsIdentifier(matches, markdownText);
  const { enriched } = identifierService.enrichIdentifiers(krt);
  const items = dedupeKrtItems(enriched, 'identifier-scan');
  return { items, scannedLength, referencesCutoff };
}

// ===================== NORMALIZATION (legacy "real" / "demo" sheets) =====================

const KIND_FROM_DETECTOR_LABEL = {
  'protocols-gemini':  'Protocols',
  'datasets-gemini':   'Datasets',
  'software-softcite': 'Software',
  'materials-gemini':  'Materials',
  'identifier-scan':   'Identifiers'
};

/** KrtEntry → legacy "real" / "demo" row shape, per detector kind. */
function toLegacyRow(item, kind, detector) {
  return {
    Kind: kind,
    'Resource Name': item.resourceName || item.canonical_name || item.name || '',
    'Resource Type': item.resourceType || item.resource_type || kind,
    Source: item.source || item.url || '',
    Identifier: item.identifier || '',
    'New/Reuse': item.newReuse || '',
    Relevance: item.detectorMeta?.relevance || item.krt_relevance || '',
    Detector: detector
  };
}

/** KrtEntry → krt-entries sheet row (canonical view). */
function toKrtEntryRow(item, manuscriptId) {
  return {
    'Manuscript ID': manuscriptId,
    'Detector': item.origin || '—',
    'Resource Type': item.resourceType || '',
    'Resource Name': item.resourceName || '',
    'Identifier': item.identifier || '',
    'Source': item.source || '',
    'New/Reuse': item.newReuse || '',
    'Confidence': typeof item.confidence === 'number' ? Number(item.confidence.toFixed(3)) : '',
    'Relevance': item.detectorMeta?.relevance || '',
    'Category': item.detectorMeta?.category || '',
    'Merged from': Array.isArray(item.mergedFrom) ? item.mergedFrom.length : 1,
    'Additional Information': item.additionalInformation || ''
  };
}

/** Demo data shape (in demo-findings/*.json) → legacy row shape. */
function demoToLegacyRow(item, kind) {
  return {
    Kind: kind,
    'Resource Name': item.canonical_name || item.name || item.resourceName || '',
    'Resource Type': item.resource_type || item.resourceType || kind,
    Source: item.source || '',
    Identifier: item.identifier || item.url || '',
    'New/Reuse': item.newReuse || '',
    Relevance: item.krt_relevance || '',
    Detector: 'demo'
  };
}

/** Extract demo resources from the demo JSON for the legacy "demo" sheet. */
function getDemoResources(demoData) {
  const rows = [];
  for (const item of demoData.protocolMentions?.items || []) rows.push(demoToLegacyRow(item, 'Protocols'));
  for (const item of demoData.datasetMentions?.items  || []) rows.push(demoToLegacyRow(item, 'Datasets'));
  for (const item of demoData.softwareMentions?.items || []) rows.push(demoToLegacyRow(item, 'Software'));
  for (const item of demoData.labMaterialMentions?.items || []) rows.push(demoToLegacyRow(item, 'Materials'));
  return rows.sort((a, b) =>
    a.Kind.localeCompare(b.Kind) || a['Resource Name'].localeCompare(b['Resource Name']));
}

// ===================== CONCURRENCY POOL =====================

async function runPool(tasks, concurrency) {
  const results = [];
  const executing = new Set();
  for (const task of tasks) {
    const p = task().then(r => { executing.delete(p); return r; });
    executing.add(p);
    results.push(p);
    if (executing.size >= concurrency) await Promise.race(executing);
  }
  return Promise.all(results);
}

// ===================== PER-MANUSCRIPT =====================

async function processManuscript(ms, serviceStatus) {
  const startTime = Date.now();
  const result = {
    manuscriptId: ms.manuscriptId,
    dasDemo: '',
    dasReal: 'SKIP',
    mdLengthDemo: 0,
    mdLengthReal: 'SKIP',
    durationSec: 0,
    realResources: [],   // legacy "real" sheet rows
    demoResources: [],   // legacy "demo" sheet rows
    krtItems: [],        // canonical KrtEntry rows for the krt-entries sheet
    identifierBreakdown: null,
    errors: []
  };

  // Demo data
  const demoData = JSON.parse(fs.readFileSync(ms.jsonPath, 'utf-8'));
  result.dasDemo = (demoData.das || '').trim();
  if (ms.mdPath) result.mdLengthDemo = fs.readFileSync(ms.mdPath, 'utf-8').length;
  result.demoResources = getDemoResources(demoData);

  // Load PDF / markdown lazily as needed
  let pdfBuffer = null;
  const needsPdf = ['das', 'markdown', 'software', 'materials']
    .some(t => isTypeEnabled(t) && serviceStatus[t]?.available);
  if (needsPdf && ms.pdfPath) {
    pdfBuffer = fs.readFileSync(ms.pdfPath);
  }
  const fileName = `${ms.manuscriptId}.pdf`;
  let realMarkdown = null;

  // --- DAS Extraction ---
  if (isTypeEnabled('das') && serviceStatus.das?.available && pdfBuffer) {
    try {
      console.log(`  [${ms.manuscriptId}] DAS…`);
      result.dasReal = (await runDAS(pdfBuffer, fileName)).trim();
    } catch (err) {
      result.dasReal = 'ERROR';
      result.errors.push(`DAS: ${err.message}`);
    }
  }

  // --- Markdown Convert ---
  if (isTypeEnabled('markdown') && serviceStatus.markdown?.available && pdfBuffer) {
    try {
      console.log(`  [${ms.manuscriptId}] markdown…`);
      realMarkdown = await runMarkdownConvert(pdfBuffer, fileName);
      result.mdLengthReal = realMarkdown.length;
    } catch (err) {
      result.mdLengthReal = 'ERROR';
      result.errors.push(`Markdown: ${err.message}`);
    }
  }

  // Use real markdown if available, otherwise fall back to demo markdown.
  let markdownForDetection = realMarkdown;
  if (!markdownForDetection && ms.mdPath) {
    markdownForDetection = fs.readFileSync(ms.mdPath, 'utf-8');
  }

  // --- Pipeline-driven detectors ---
  const pipelineRuns = [
    ['protocols',  'Protocols',  () => markdownForDetection && runProtocols(markdownForDetection)],
    ['datasets',   'Datasets',   () => markdownForDetection && runDatasets(markdownForDetection)],
    ['software',   'Software',   () => pdfBuffer && runSoftware(pdfBuffer, fileName)],
    ['materials',  'Materials',  () => pdfBuffer && runMaterials(pdfBuffer, fileName)]
  ];
  for (const [type, kind, fn] of pipelineRuns) {
    if (!isTypeEnabled(type)) continue;
    if (!serviceStatus[type]?.available) continue;
    try {
      console.log(`  [${ms.manuscriptId}] ${type}…`);
      const items = await fn();
      if (!Array.isArray(items)) continue;
      const detectorLabel = items[0]?.origin || type;
      for (const item of items) {
        result.krtItems.push(item);
        result.realResources.push(toLegacyRow(item, kind, detectorLabel));
      }
    } catch (err) {
      result.errors.push(`${type}: ${err.message}`);
      console.error(`  [${ms.manuscriptId}] ${type} ERROR: ${err.message}`);
    }
  }

  // --- Identifier Scan ---
  if (isTypeEnabled('identifiers') && serviceStatus.identifiers?.available && markdownForDetection) {
    try {
      console.log(`  [${ms.manuscriptId}] identifiers…`);
      const { items, scannedLength, referencesCutoff } = await runIdentifiers(markdownForDetection);
      for (const item of items) {
        result.krtItems.push(item);
        result.realResources.push(toLegacyRow(item, 'Identifiers', 'identifier-scan'));
      }
      result.identifierBreakdown = {
        HIGH:   items.filter(i => i.detectorMeta?.relevance === 'HIGH').length,
        MEDIUM: items.filter(i => i.detectorMeta?.relevance === 'MEDIUM').length,
        LOW:    items.filter(i => i.detectorMeta?.relevance === 'LOW').length,
        total:  items.length,
        scannedLength,
        referencesCutoff
      };
    } catch (err) {
      result.errors.push(`Identifiers: ${err.message}`);
      console.error(`  [${ms.manuscriptId}] identifiers ERROR: ${err.message}`);
    }
  }

  result.realResources.sort((a, b) =>
    a.Kind.localeCompare(b.Kind) || a['Resource Name'].localeCompare(b['Resource Name']));
  result.durationSec = ((Date.now() - startTime) / 1000).toFixed(1);
  return result;
}

// ===================== XLSX GENERATION =====================

function buildWorkbook(results) {
  const wb = XLSX.utils.book_new();

  // --- Summary tab ---
  const summaryData = results.map(r => {
    const dasMatch = r.dasReal === 'SKIP' ? 'SKIP' : r.dasReal === 'ERROR' ? 'ERROR'
      : (!r.dasDemo && !r.dasReal) ? 'BOTH_EMPTY'
      : r.dasDemo === r.dasReal ? 'YES' : 'NO';

    // Count by kind. Identifier-scan items have Kind='Identifiers' which is
    // its own column; the other detectors split across Protocols/Datasets/
    // Software/Materials.
    const cnt = (kind) => r.realResources.filter(x => x.Kind === kind && x.Detector !== 'identifier-scan').length;
    const demoCnt = (kind) => r.demoResources.filter(x => x.Kind === kind).length;

    const idBreak = r.identifierBreakdown || { HIGH: 0, MEDIUM: 0, LOW: 0, total: 0 };

    return {
      'Manuscript ID': r.manuscriptId,
      'DAS Match': dasMatch,
      'MD Length (demo)': r.mdLengthDemo,
      'MD Length (real)': r.mdLengthReal,
      'Protocols (demo)': demoCnt('Protocols'),
      'Protocols (real)': cnt('Protocols'),
      'Datasets (demo)':  demoCnt('Datasets'),
      'Datasets (real)':  cnt('Datasets'),
      'Software (demo)':  demoCnt('Software'),
      'Software (real)':  cnt('Software'),
      'Materials (demo)': demoCnt('Materials'),
      'Materials (real)': cnt('Materials'),
      'Identifiers Total':  idBreak.total,
      'Identifiers HIGH':   idBreak.HIGH,
      'Identifiers MEDIUM': idBreak.MEDIUM,
      'Identifiers LOW':    idBreak.LOW,
      'Duration (s)': r.durationSec,
      'Errors': r.errors.join('; ')
    };
  });

  const summarySheet = XLSX.utils.json_to_sheet(summaryData);
  summarySheet['!cols'] = [
    { wch: 28 }, { wch: 12 }, { wch: 14 }, { wch: 14 },
    { wch: 16 }, { wch: 16 }, { wch: 16 }, { wch: 16 },
    { wch: 16 }, { wch: 16 }, { wch: 16 }, { wch: 16 },
    { wch: 14 }, { wch: 14 }, { wch: 16 }, { wch: 14 },
    { wch: 12 }, { wch: 40 }
  ];
  XLSX.utils.book_append_sheet(wb, summarySheet, 'Summary');

  // --- Real & Demo tabs (legacy row shape) ---
  const allReal = [];
  const allDemo = [];
  for (const r of results) {
    for (const row of r.realResources) allReal.push({ 'Manuscript ID': r.manuscriptId, ...row });
    for (const row of r.demoResources) allDemo.push({ 'Manuscript ID': r.manuscriptId, ...row });
  }
  const legacyCols = [
    { wch: 28 }, { wch: 12 }, { wch: 40 }, { wch: 16 }, { wch: 30 }, { wch: 40 }, { wch: 10 }, { wch: 10 }, { wch: 16 }
  ];
  const realSheet = XLSX.utils.json_to_sheet(allReal);
  realSheet['!cols'] = legacyCols;
  XLSX.utils.book_append_sheet(wb, realSheet, 'real');
  const demoSheet = XLSX.utils.json_to_sheet(allDemo);
  demoSheet['!cols'] = legacyCols;
  XLSX.utils.book_append_sheet(wb, demoSheet, 'demo');

  // --- krt-entries tab (canonical KrtEntry shape) ---
  // One row per canonical item per manuscript. This is the post-dedup view
  // each detector module persists to SubmissionJob.result.data.items.
  const allKrt = [];
  for (const r of results) {
    for (const item of r.krtItems) {
      allKrt.push(toKrtEntryRow(item, r.manuscriptId));
    }
  }
  const krtSheet = XLSX.utils.json_to_sheet(allKrt);
  krtSheet['!cols'] = [
    { wch: 28 }, { wch: 18 }, { wch: 14 }, { wch: 40 },
    { wch: 40 }, { wch: 30 }, { wch: 10 }, { wch: 11 },
    { wch: 10 }, { wch: 12 }, { wch: 11 }, { wch: 60 }
  ];
  XLSX.utils.book_append_sheet(wb, krtSheet, 'krt-entries');

  return wb;
}

// ===================== MAIN =====================

async function main() {
  console.log('=== Benchmark Detection Script ===\n');

  const manuscripts = discoverManuscripts();
  if (manuscripts.length === 0) {
    console.error('No manuscripts found. Check demo-findings dir or --manuscript / --no-krt-only filters.');
    process.exit(1);
  }
  console.log(`Found ${manuscripts.length} manuscript(s)${NO_KRT_ONLY ? ' (no-KRT filter applied)' : ''}\n`);

  const serviceStatus = checkServiceAvailability();
  console.log('Service availability:');
  for (const [type, status] of Object.entries(serviceStatus)) {
    const enabled = isTypeEnabled(type);
    const icon = !enabled ? '⊘' : status.available ? '✓' : '✗';
    console.log(`  ${icon} ${type}: ${enabled ? status.reason : 'skipped by user'}`);
  }

  // Warm the identifier-scan index so its size shows up in startup logs and
  // the build cost is paid once instead of inside the first task.
  if (isTypeEnabled('identifiers') && serviceStatus.identifiers?.available) {
    await ensureIdentifierIndex();
  }

  console.log(`\nConcurrency: ${CONCURRENCY}\n`);

  const tasks = manuscripts.map(ms => () => {
    console.log(`▸ Processing ${ms.manuscriptId}…`);
    return processManuscript(ms, serviceStatus);
  });
  const results = await runPool(tasks, CONCURRENCY);

  // Generate XLSX
  const wb = buildWorkbook(results);
  const today = new Date().toISOString().split('T')[0];
  const outputPath = OUTPUT_PATH || path.join(__dirname, `benchmark-results-${today}.xlsx`);
  const buffer = XLSX.write(wb, { type: 'buffer', bookType: 'xlsx' });
  fs.writeFileSync(outputPath, buffer);

  console.log(`\n=== Done ===`);
  console.log(`Processed: ${results.length} manuscript(s)`);
  const errorCount = results.filter(r => r.errors.length > 0).length;
  if (errorCount > 0) console.log(`Errors: ${errorCount} manuscript(s) had errors`);
  console.log(`Output: ${outputPath}`);
}

main().catch(err => { console.error('Fatal error:', err); process.exit(1); });
